def get_random_user_agent(seed: int | None = None) -> str:
    """Return a random User-Agent string.

    If seed is provided, the selection is deterministic.
    """
    rng = random.Random(seed)
    return rng.choice(USER_AGENTS)


class UserAgentRotator:
    """Rotates through User-Agent strings using round-robin or random selection."""

    def __init__(
        self,
        strategy: str = "round-robin",
        seed: int | None = None,
    ) -> None:
        self._strategy = strategy
        self._rng = random.Random(seed)
        self._index = 0

    def get_next(self) -> str:
        """Return the next User-Agent string."""
        if self._strategy == "random":
            return self._rng.choice(USER_AGENTS)
        # round-robin
        ua = USER_AGENTS[self._index % len(USER_AGENTS)]
        self._index += 1
        return ua
